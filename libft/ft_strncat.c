#include "libft.h"

char	*ft_strncat(char *dest, const char *src, size_t n)
{
	char *cpy;

	if (n)
	{
		cpy = dest;
		while (*cpy)
			cpy++;
		while (*src && n--)
			*cpy++ = *src++;
		*cpy = '\0';
	}
	return (dest);
}
