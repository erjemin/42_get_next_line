int			ft_atoi(const char *s)
{
	unsigned long long	ret;
	int					sign;

	sign = 1;
	ret = 0;
	while (*s && (*s == ' ' || *s == '\t' || *s == '\n' || *s == '\r' || \
				*s == '\v' || *s == '\f'))
		s++;
	if (*s == '+' || *s == '-')
		sign = -(*s++ - (int)',');
	while (*s && *s >= '0' && *s <= '9')
		ret = (ret * 10) + (*s++ - '0');
	if (ret > 9223372036854775807)
		return (sign == -1 ? 0 : -1);
	else
		return (ret * sign);
}
