#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*result;

	result = (char *)s;
	while (*result)
		result++;
	while (result >= s)
		if (*result == (char)c)
			return (result);
		else
			result--;
	return (NULL);
}
